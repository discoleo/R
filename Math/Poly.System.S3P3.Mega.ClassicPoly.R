### Classic solver:
solve.Y21.Clasic.S3P3 = function(R, b, debug=FALSE) {
	coeff = rev(coef.S3P3(R, b));
	x = roots(coeff)
	solver = solve.yz.Y21.Clasic.S3P3.gen();
	return(solver(x, R=R, b=b))
}

