solve.yz.Y21.Clasic.S3P3.gen = function() {
	z.coeff = coef.z.Y21();
	z.div.c = coef.zdiv.Y21();
	#
	solve.yz.Y21.Clasic.S3P3 = function(x, R, b) {
	### col
	# R = 1;
	# b1 = 2;
	# b2 = 3;
	# x = 4;
	b2 = b[1]; b1 = b[2];
	Rpow  =  R^seq(0, max(z.coeff$m[,1], z.div.c$m[,1]));
	b1pow = b1^seq(0, max(z.coeff$m[,2], z.div.c$m[,2]));
	b2pow = b2^seq(0, max(z.coeff$m[,3], z.div.c$m[,3]));
	solve.z.vf = function(x) {
		xpow =  x^seq(0, max(z.coeff$m[,4], z.div.c$m[,4]));
		#
		z = sum(z.coeff$coeff * Rpow[z.coeff$m[,1] + 1] *
			b1pow[z.coeff$m[,2] + 1] * b2pow[z.coeff$m[,3] + 1] * xpow[z.coeff$m[,4] + 1])
		z.div = sum(z.div.c$coeff * Rpow[z.div.c$m[,1] + 1] *
			b1pow[z.div.c$m[,2] + 1] * b2pow[z.div.c$m[,3] + 1] * xpow[z.div.c$m[,4] + 1])
		return(- z/z.div);
	}
	z = sapply(x, solve.z.vf);
	y = R*b1 + R*b2^2 - b1*b2^2*z - b1*x^3 - b2^3*z^2;
	y.div = - R*b2 - b1^2 + b2*x^3;
	y = -y / y.div;
	
		return(cbind(x=x, y=y, z=z));
	}
	return(solve.yz.Y21.Clasic.S3P3)
}

