### Orders 4 to 7:

polynom.f = function(K, s, n=length(s) + 1) {
	# - precomputed formulas for the coefficients;
	# - s = shifts of the root-components;
	# - does NOT include s0 (as it is a trivial term);
	if(length(s) + 1 < n) s = c(s, rep(0, n - length(s) - 1))
	
	### Order 4:
	if(n == 4) {
		s1 = s[1]; s2 = s[2]; s3 = s[3];
		coeff = c(
			(- K*s1^4 - 4*K^2*s1*s2^2*s3 + 2*K^2*s1^2*s3^2 + K^2*s2^4 - K^3*s3^4),
			(- 4*K*s1^2*s2 - 4*K^2*s2*s3^2),
			(- 4*K*s1*s3 - 2*K*s2^2), 0, 1)
	}
	
	### Order 5:
	else if(n == 5) {
		s1 = s[1]; s2 = s[2]; s3 = s[3]; s4 = s[4];
		coeff = c(
			(- K*s1^5 + 5*K^2*s1*s2^3*s3 - 5*K^2*s1^2*s2*s3^2 - 5*K^2*s1^2*s2^2*s4 + 5*K^2*s1^3*s3*s4 +
				- K^2*s2^5 + 5*K^3*s1*s2*s4^3 - 5*K^3*s1*s3^2*s4^2 + 5*K^3*s2*s3^3*s4 - 5*K^3*s2^2*s3*s4^2 +
				- K^3*s3^5 - K^4*s4^5),
			(- 5*K*s1^3*s2 - 5*K^2*s1*s2*s3*s4 - 5*K^2*s1*s3^3 + 5*K^2*s1^2*s4^2 + 5*K^2*s2^2*s3^2 +
				- 5*K^2*s2^3*s4 - 5*K^3*s3*s4^3),
			(- 5*K*s1*s2^2 - 5*K*s1^2*s3 - 5*K^2*s2*s4^2 - 5*K^2*s3^2*s4),
			(- 5*K*s1*s4 - 5*K*s2*s3), 0, 1)
	}
	
	### Order 6:
	else if(n == 6) {
		s1 = s[1]; s2 = s[2]; s3 = s[3]; s4 = s[4]; s5 = s[5];
		coeff = c(
			(- K*s1^6 - 6*K^2*s1*s2^4*s3 + 9*K^2*s1^2*s2^2*s3^2 + 6*K^2*s1^2*s2^3*s4 - 12*K^2*s1^3*s2*s3*s4 +
				- 6*K^2*s1^3*s2^2*s5 - 2*K^2*s1^3*s3^3 + 6*K^2*s1^4*s3*s5 + 3*K^2*s1^4*s4^2 + K^2*s2^6 +
				12*K^3*s1*s2*s3*s4^3 - 18*K^3*s1*s2^2*s4^2*s5 - 6*K^3*s1*s3^3*s4^2 + 6*K^3*s1*s3^4*s5 +
				18*K^3*s1^2*s2*s4*s5^2 - 9*K^3*s1^2*s3^2*s5^2 - 3*K^3*s1^2*s4^4 - 2*K^3*s1^3*s5^3 +
				6*K^3*s2*s3^4*s4 - 9*K^3*s2^2*s3^2*s4^2 - 6*K^3*s2^2*s3^3*s5 + 12*K^3*s2^3*s3*s4*s5 +
				2*K^3*s2^3*s4^3 - 3*K^3*s2^4*s5^2 - K^3*s3^6 + 6*K^4*s1*s3*s5^4 - 6*K^4*s1*s4^2*s5^3 +
				- 12*K^4*s2*s3*s4*s5^3 + 6*K^4*s2*s4^3*s5^2 + 3*K^4*s2^2*s5^4 - 6*K^4*s3*s4^4*s5 +
				9*K^4*s3^2*s4^2*s5^2 - 2*K^4*s3^3*s5^3 + K^4*s4^6 - K^5*s5^6),
			(- 6*K*s1^4*s2 + 12*K^2*s1*s2*s3^3 - 12*K^2*s1*s2^3*s5 - 18*K^2*s1^2*s3^2*s4 +
				12*K^2*s1^3*s4*s5 - 6*K^2*s2^3*s3^2 + 6*K^2*s2^4*s4 + 12*K^3*s1*s2*s5^3 - 12*K^3*s1*s4^3*s5 +
				- 18*K^3*s2*s3^2*s5^2 + 6*K^3*s2*s4^4 - 6*K^3*s3^2*s4^3 + 12*K^3*s3^3*s4*s5 - 6*K^4*s4*s5^4),
			(- 9*K*s1^2*s2^2 - 6*K*s1^3*s3 - 18*K^2*s1*s3*s4^2 + 9*K^2*s1^2*s5^2 - 18*K^2*s2^2*s3*s5 +
				9*K^2*s2^2*s4^2 + 3*K^2*s3^4 - 6*K^3*s3*s5^3 - 9*K^3*s4^2*s5^2),
			(- 12*K*s1*s2*s3 - 6*K*s1^2*s4 - 2*K*s2^3 - 6*K^2*s2*s5^2 - 12*K^2*s3*s4*s5 - 2*K^2*s4^3),
			(- 6*K*s1*s5 - 6*K*s2*s4 - 3*K*s3^2), 0, 1)
	}
	
	### Order 7:
	else if(n == 7) {
		# too long;
		coeff = coeff.order7.f(K, s);
	}
	#
	class(coeff) = c(class(coeff), "polynomial")
	### Roots:
	# all n roots: computed exactly (with radicals)!
	r = roots.f(K, c(0, s)) # include s0 = 0
	#
	return(list(p=coeff, x=r))
}
